def soma(a,b):
    s = a + b
    return(s)
def subt(a,b):  
    sub = a - b  
    return(sub)
def divs(a,b):   
    div = a / b 
    return(div)
def mult(a, b):
    mul = a * b
    return(mul)

n1 = int(input("Digite um numero:"))
n2 = int(input("Digite um numero:"))

print(soma(n1,n2))
print(subt(n1,n2))
print(divs(n1,n2))
